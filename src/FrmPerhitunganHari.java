    private void btnSelisihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelisihActionPerformed
        try {
            java.util.Date tgl1 = SeliKal1.getDate();
            java.util.Date tgl2 = SeliKal2.getDate();

            if (tgl1 == null || tgl2 == null) {
                JOptionPane.showMessageDialog(this,
                    "Silakan pilih dua tanggal terlebih dahulu!",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            LocalDate date1 = new java.sql.Date(tgl1.getTime()).toLocalDate();
            LocalDate date2 = new java.sql.Date(tgl2.getTime()).toLocalDate();

            long selisih = Math.abs(ChronoUnit.DAYS.between(date1, date2));

            lblHasil.setText(String.valueOf(selisih));

            txtInfo.setText(
                "Tanggal 1: " + date1 + "\n" +
                "Tanggal 2: " + date2 + "\n" +
                "Selisih antara kedua tanggal adalah " + selisih + " hari."
            );
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi kesalahan menghitung selisih tanggal.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSelisihActionPerformed

    private void KalenderPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_KalenderPropertyChange
        if ("calendar".equals(evt.getPropertyName())) {
            java.util.Calendar cal = (java.util.Calendar) evt.getNewValue();
            if (cal != null) {
                int bulan = cal.get(java.util.Calendar.MONTH);
                int tahun = cal.get(java.util.Calendar.YEAR);
                cmbBulan.setSelectedIndex(bulan);
                spnTahun.setValue(tahun);
            }
        }
    }//GEN-LAST:event_KalenderPropertyChange

