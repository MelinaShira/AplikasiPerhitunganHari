    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        try {
            int tahun = (int) spnTahun.getValue();
            String namaBulan = (String) cmbBulan.getSelectedItem();
        
            // Konversi manual nama bulan Indonesia ke Month
            Month bulan = switch (namaBulan.toLowerCase()) {
                case "januari" -> Month.JANUARY;
                case "februari" -> Month.FEBRUARY;
                case "maret" -> Month.MARCH;
                case "april" -> Month.APRIL;
                case "mei" -> Month.MAY;
                case "juni" -> Month.JUNE;
                case "juli" -> Month.JULY;
                case "agustus" -> Month.AUGUST;
                case "september" -> Month.SEPTEMBER;
                case "oktober" -> Month.OCTOBER;
                case "november" -> Month.NOVEMBER;
                case "desember" -> Month.DECEMBER;
                default -> throw new IllegalArgumentException("Bulan tidak valid!");
            };

            LocalDate tanggalAwal = LocalDate.of(tahun, bulan, 1);
            int jumlahHari = tanggalAwal.lengthOfMonth();
            LocalDate tanggalAkhir = tanggalAwal.withDayOfMonth(jumlahHari);

            lblHasil.setText(String.valueOf(jumlahHari));

            txtInfo.setText(
                "Bulan: " + namaBulan + "\n" +
                "Tahun: " + tahun + "\n" +
                "Hari pertama: " + tanggalAwal.getDayOfWeek()
                    .getDisplayName(TextStyle.FULL, new Locale("id")) + "\n" +
                "Hari terakhir: " + tanggalAkhir.getDayOfWeek()
                    .getDisplayName(TextStyle.FULL, new Locale("id"))
            );
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi kesalahan input!\nPastikan tahun dan bulan diisi dengan benar.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHitungActionPerformed

